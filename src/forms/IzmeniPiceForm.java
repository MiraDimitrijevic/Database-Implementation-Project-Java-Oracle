    private void prepareData() {
        txtNazivPica.setText(pice.getNazivPica());
        txtKolicina.setText(String.valueOf(pice.getKolicinaNaStanju()));
        txtTrenutnaCena.setText(String.valueOf(pice.getTrenutnaCena()));
        try {
            ArrayList<VrstaPica> vps = ClientController.getInstance().getAllVrstaPica();
            System.out.println(vps == null);
            cmbVrstaPica.removeAllItems();
            for (VrstaPica vp : vps) {
                cmbVrstaPica.addItem(vp);
            }
            cmbVrstaPica.setSelectedItem(pice.getVrstaPica());
            ArrayList<JedinicaMere> jms = ClientController.getInstance().getAllJedinicaMere();
            cmbJM.removeAllItems();
            for (JedinicaMere jm : jms) {
                cmbJM.addItem(jm);
            }
            cmbJM.setSelectedItem(pice.getJedinicaMere());
        } catch (Exception ex) {
            System.out.println("Klijentova metoda pravi gresku");
            java.util.logging.Logger.getLogger(IzmeniPiceForm.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());

        }

    }
