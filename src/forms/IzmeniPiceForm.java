    private void btnIzmeniPiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniPiceActionPerformed

        try {
            String naziv = txtNazivPica.getText();
            if (naziv.isEmpty() || txtKolicina.getText().isEmpty() || txtTrenutnaCena.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju "
                        + "biti popunjena!",
                        "Uspesno cuvanje", JOptionPane.PLAIN_MESSAGE);
                return;
            }
            int kolicinaNaStanju = Integer.valueOf(txtKolicina.getText());
            JedinicaMere jm = (JedinicaMere) cmbJM.getSelectedItem();
            VrstaPica vp = (VrstaPica) cmbVrstaPica.getSelectedItem();
            Double trenutnaCena = Double.valueOf(txtTrenutnaCena.getText().replace(',', '.'));
            Pice p = new Pice(pice.getSifraPica(), naziv, kolicinaNaStanju, jm, vp, trenutnaCena, null);
            TableModelCena tmc = (TableModelCena) tblCena.getModel();
            if (tmc.getLista().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Pice mora imati cenu!");
                return;
            }
            p.setOcekivanaCena(tmc.getOcekivanaCena());
            ClientController.getInstance().updatePice(p);
            JOptionPane.showMessageDialog(this, "Pice uspesno izmenjeno!",
                    "Uspesna izmena", JOptionPane.PLAIN_MESSAGE);
        } catch (Exception ex) {
            java.util.logging.Logger.getLogger(IzmeniPiceForm.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());

        }

    }//GEN-LAST:event_btnIzmeniPiceActionPerformed

    private void btnSacuvajCenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajCenuActionPerformed
        if (txtIznosCene.getText().isEmpty() || txtIDatumOd.getText().isEmpty() || txtDatumDo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja moraju "
                    + "biti popunjena!",
                    "Uspesno cuvanje", JOptionPane.PLAIN_MESSAGE);
            return;
        }
        double iznos = Double.valueOf(txtIznosCene.getText().replace(',', '.'));
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy.");
        try {
            Date datod = sdf.parse(txtIDatumOd.getText());
            Date datdo = sdf.parse(txtDatumDo.getText());
            Cena cena = new Cena(pice, datod, datdo, iznos);

            if (cuvanje) {
                ClientController.getInstance().createCena(cena);
                JOptionPane.showMessageDialog(this, "Cena uspesno kreirana!",
                        "Uspesno kreiranje cene", JOptionPane.PLAIN_MESSAGE);
            } else {
                ClientController.getInstance().updateCena(cena);
                JOptionPane.showMessageDialog(this, "Cena uspesno izmenjena!",
                        "Uspesna izmena cene", JOptionPane.PLAIN_MESSAGE);
                btnIzmeniCenu.setEnabled(true);
                btnIzmeniPice.setEnabled(true);
                btnObrisiCenu.setEnabled(true);
                btnSacuvajCenu.setText("Sacuvaj cenu");
                cuvanje = true;
                cenaIzmena = null;

            }
            txtIznosCene.setText("");
            txtIDatumOd.setText("");
            txtDatumDo.setText("");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            java.util.logging.Logger.getLogger(IzmeniPiceForm.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSacuvajCenuActionPerformed

    private void btnIzmeniCenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniCenuActionPerformed
        int row = tblCena.getSelectedRow();
        if (row != -1) {
            TableModelCena tmc = (TableModelCena) tblCena.getModel();
            Cena c = tmc.getCena(row);
            btnIzmeniCenu.setEnabled(false);
            btnIzmeniPice.setEnabled(false);
            btnObrisiCenu.setEnabled(false);
            btnSacuvajCenu.setText("Izmeni cenu");
            cuvanje = false;
            cenaIzmena = c;
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy.");
            txtDatumDo.setText(sdf.format(cenaIzmena.getDatumDo()));
            txtIDatumOd.setText(sdf.format(cenaIzmena.getDatumOd()));

        }
    }//GEN-LAST:event_btnIzmeniCenuActionPerformed

    private void btnObrisiCenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiCenuActionPerformed
        int row = tblCena.getSelectedRow();
        if (row != -1) {
            TableModelCena tmc = (TableModelCena) tblCena.getModel();
            Cena c = tmc.getCena(row);
            try {
                if (tmc.getLista().isEmpty()) {
                    JOptionPane.showMessageDialog(this,"Ne mozete obrisati jedinu cenu!");
                    return;

                }
                ClientController.getInstance().deleteCena(c);
                JOptionPane.showMessageDialog(this, "Cena uspesno obrisana!",
                        "Uspesno brisanje cene", JOptionPane.PLAIN_MESSAGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
                java.util.logging.Logger.getLogger(IzmeniPiceForm.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btnObrisiCenuActionPerformed


